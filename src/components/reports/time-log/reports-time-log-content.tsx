"use client";

import { useCallback, useEffect, useMemo, useRef, useState } from "react";

import { Download, Loader2 } from "lucide-react";

import { CopilotErrorAlert } from "@/components/copilot/copilot-error-alert";
import { Button } from "@/components/ui/button";
import { ReportsTimeLogAlerts } from "@/components/reports/time-log/reports-time-log-alerts";
import { ReportsTimeLogEmptyState } from "@/components/reports/time-log/reports-time-log-empty-state";
import { ReportsTimeLogFilters } from "@/components/reports/time-log/reports-time-log-filters";
import { ReportsTimeLogStaleBanner } from "@/components/reports/time-log/reports-time-log-stale-banner";
import {
  ReportsTimeLogTable,
  ReportsTimeLogTableSkeleton,
} from "@/components/reports/time-log/reports-time-log-table";
import { AssigneeVisibilityCombobox } from "@/components/sprints/stats/assignee-visibility-combobox";
import { useHoursReport } from "@/hooks/reports/use-hours-report";
import type { AdoCatalogSnapshot } from "@/lib/ado/types";
import { normalizePersonName } from "@/lib/filters/person-name";
import { filterHoursReportByVisibility } from "@/lib/reports/hours/filter-hours-report-by-visibility";
import type {
  HoursReportPeriodSchema,
  HoursReportRequestSchema,
} from "@/lib/schemas/reports-hours";
import { getTodayDateKey } from "@/lib/time-log/working-date-default";
import { appToast } from "@/lib/toast";
import { useAssigneeVisibilityStore } from "@/store/assignee-visibility-store";

export type ReportsTimeLogContentProps = {
  catalog: AdoCatalogSnapshot;
  initialScopes: { projectIds: string[]; teamIds: string[] };
  adoExecutionReady: boolean;
};

function defaultPeriod(): HoursReportPeriodSchema {
  const todayKey = getTodayDateKey();
  return { kind: "month", monthKey: todayKey.slice(0, 7) };
}

function defaultMonthKey(): string {
  const p = defaultPeriod();
  return p.kind === "month" ? p.monthKey : "";
}

export function ReportsTimeLogContent({
  catalog,
  initialScopes,
  adoExecutionReady,
}: Readonly<ReportsTimeLogContentProps>) {
  const [period, setPeriod] = useState<HoursReportPeriodSchema>(defaultPeriod());
  const [rangeFrom, setRangeFrom] = useState<string>(getTodayDateKey());
  const [rangeTo, setRangeTo] = useState<string>(getTodayDateKey());
  const [projectIds, setProjectIds] = useState<string[]>(initialScopes.projectIds);
  const [teamIds, setTeamIds] = useState<string[]>(initialScopes.teamIds);
  const [nameFilter, setNameFilter] = useState<string>("");
  const [downloadingExcel, setDownloadingExcel] = useState(false);

  const hoursReport = useHoursReport();
  const autoGeneratedRef = useRef(false);

  // Sin selección explícita el reporte cubre todo el catálogo, de modo que
  // siempre viajen proyectos/equipos concretos y el servidor pueda cargar el
  // roster de cada equipo (las personas que deben salir en la tabla).
  const allProjectNames = useMemo(
    () => catalog.projects.map((p) => p.name),
    [catalog.projects],
  );
  const allTeamNames = useMemo(
    () => catalog.teams.map((t) => t.name),
    [catalog.teams],
  );

  /**
   * Construye el payload del reporte. Acepta overrides para que los handlers
   * de filtros (projects/teams/period) puedan pasar el valor NUEVO
   * directamente al `markStale`. Si no se pasa override, usa el estado actual
   * capturado en el closure — importante para que `markStale` detecte cambios
   * de projects/teams (antes el closure tenía los IDs viejos y nunca marcaba
   * stale al cambiar un proyecto o equipo).
   */
  const buildPayload = useCallback(
    (next?: {
      period?: HoursReportPeriodSchema;
      projectIds?: string[];
      teamIds?: string[];
    }): HoursReportRequestSchema => {
      const periodValue = next?.period ?? period;
      const projectIdsValue = next?.projectIds ?? projectIds;
      const teamIdsValue = next?.teamIds ?? teamIds;
      return {
        period: periodValue,
        scopes: {
          projectIds: projectIdsValue.length > 0 ? projectIdsValue : allProjectNames,
          teamIds: teamIdsValue.length > 0 ? teamIdsValue : allTeamNames,
        },
      };
    },
    [period, projectIds, teamIds, allProjectNames, allTeamNames],
  );

  const payload = useMemo(
    () => buildPayload(),
    [buildPayload],
  );

  const allRows = useMemo(
    () => hoursReport.result?.rows ?? [],
    [hoursReport.result],
  );

  // Mostrar/ocultar personas: mismo store que el reporte por sprint, para que
  // las personas ocultas se compartan entre ambas pantallas.
  const hiddenPersons = useAssigneeVisibilityStore((s) => s.hidden);
  const togglePersonVisibility = useAssigneeVisibilityStore((s) => s.toggle);
  const allPersons = useMemo(
    () =>
      Array.from(new Set(allRows.map((row) => row.personDisplayName))).sort(
        (a, b) => a.localeCompare(b, "es"),
      ),
    [allRows],
  );

  // Mismo filtrado que aplica el POST del Excel, para que tabla y archivo
  // siempre coincidan.
  const visibleRows = useMemo(
    () =>
      hoursReport.result
        ? filterHoursReportByVisibility(hoursReport.result, hiddenPersons).rows
        : [],
    [hoursReport.result, hiddenPersons],
  );

  const filteredRows = useMemo(() => {
    const query = normalizePersonName(nameFilter);
    if (!query) return visibleRows;
    return visibleRows.filter((row) =>
      normalizePersonName(row.personDisplayName).includes(query),
    );
  }, [visibleRows, nameFilter]);

  const onNameFilterChange = useCallback((value: string) => {
    setNameFilter(value);
  }, []);

  const onGenerate = useCallback(async () => {
    await hoursReport.generate(payload);
  }, [hoursReport, payload]);

  const markStaleNow = useCallback(
    (nextPeriod: HoursReportPeriodSchema) => {
      hoursReport.markStale(buildPayload({ period: nextPeriod }));
    },
    [hoursReport, buildPayload],
  );

  useEffect(() => {
    if (autoGeneratedRef.current) return;
    if (!adoExecutionReady) return;
    autoGeneratedRef.current = true;
    void hoursReport.generate(payload);
  }, [hoursReport, payload, adoExecutionReady]);

  const year = period.kind === "month" ? Number(period.monthKey.slice(0, 4)) : Number(getTodayDateKey().slice(0, 4));
  const monthKey = period.kind === "month" ? period.monthKey : `${year}-01`;

  const switchToMonth = useCallback(() => {
    const next: HoursReportPeriodSchema = { kind: "month", monthKey: defaultMonthKey() };
    setPeriod(next);
    markStaleNow(next);
  }, [markStaleNow]);

  const switchToRange = useCallback(() => {
    const next: HoursReportPeriodSchema = { kind: "range", fromIso: rangeFrom, toIso: rangeTo };
    setPeriod(next);
    markStaleNow(next);
  }, [rangeFrom, rangeTo, markStaleNow]);

  const onYearChange = useCallback(
    (y: number) => {
      if (period.kind !== "month") return;
      const next: HoursReportPeriodSchema = {
        kind: "month",
        monthKey: `${y}-${period.monthKey.slice(5, 7)}`,
      };
      setPeriod(next);
      markStaleNow(next);
    },
    [period, markStaleNow],
  );

  const onMonthKeyChange = useCallback(
    (k: string) => {
      const next: HoursReportPeriodSchema = { kind: "month", monthKey: k };
      setPeriod(next);
      markStaleNow(next);
    },
    [markStaleNow],
  );

  const onRangeFromChange = useCallback(
    (iso: string) => {
      setRangeFrom(iso);
      const next: HoursReportPeriodSchema =
        period.kind === "range" ? { kind: "range", fromIso: iso, toIso: period.toIso } : period;
      setPeriod(next);
      markStaleNow(next);
    },
    [period, markStaleNow],
  );

  const onRangeToChange = useCallback(
    (iso: string) => {
      setRangeTo(iso);
      const next: HoursReportPeriodSchema =
        period.kind === "range" ? { kind: "range", fromIso: period.fromIso, toIso: iso } : period;
      setPeriod(next);
      markStaleNow(next);
    },
    [period, markStaleNow],
  );

  const onProjectIdsChange = useCallback(
    (ids: string[]) => {
      setProjectIds(ids);
      // Pasamos los IDs nuevos al markStale para que la comparación detecte
      // el cambio (con el closure solo teníamos los IDs anteriores).
      hoursReport.markStale(buildPayload({ projectIds: ids }));
    },
    [hoursReport, buildPayload],
  );

  const onTeamIdsChange = useCallback(
    (ids: string[]) => {
      setTeamIds(ids);
      hoursReport.markStale(buildPayload({ teamIds: ids }));
    },
    [hoursReport, buildPayload],
  );

  const onExportExcel = useCallback(async () => {
    if (hoursReport.status === "stale") {
      appToast.warning("El reporte está desactualizado. Regenera antes de exportar.");
      return;
    }
    if (!hoursReport.result || hoursReport.result.rows.length === 0) {
      appToast.warning("No hay datos para exportar.");
      return;
    }
    setDownloadingExcel(true);
    try {
      // Mismo payload que la generación (vacío ⇒ todo el catálogo) más las
      // personas ocultas del store de visibilidad, para que el Excel refleje
      // exactamente la tabla. Todo viaja en el body del POST, no por la URL.
      await hoursReport.downloadExcel({
        ...payload,
        hiddenPersons: allPersons.filter((person) => hiddenPersons.has(person)),
      });
    } finally {
      setDownloadingExcel(false);
    }
  }, [hoursReport, payload, allPersons, hiddenPersons]);

  const showStale = hoursReport.status === "stale";
  const showError = hoursReport.status === "error" || Boolean(hoursReport.errorMessage);
  const hasGenerated = hoursReport.status === "ready" || hoursReport.status === "stale";
  const showEmpty =
    hasGenerated &&
    hoursReport.result !== null &&
    hoursReport.result.rows.length === 0;
  const showExport = hasGenerated && (hoursReport.result?.rows.length ?? 0) > 0;

  return (
    <div className="flex flex-col gap-6">
      <ReportsTimeLogFilters
        projects={catalog.projects}
        teams={catalog.teams}
        period={period}
        year={year}
        monthKey={monthKey}
        rangeFrom={rangeFrom}
        rangeTo={rangeTo}
        selectedProjectIds={projectIds}
        selectedTeamIds={teamIds}
        nameFilter={nameFilter}
        isStale={hoursReport.status === "stale"}
        onPeriodChange={(kind) => (kind === "month" ? switchToMonth() : switchToRange())}
        onYearChange={onYearChange}
        onMonthKeyChange={onMonthKeyChange}
        onRangeFromChange={onRangeFromChange}
        onRangeToChange={onRangeToChange}
        onProjectIdsChange={onProjectIdsChange}
        onTeamIdsChange={onTeamIdsChange}
        onNameFilterChange={onNameFilterChange}
        onGenerate={onGenerate}
        generating={hoursReport.status === "generating"}
        payload={payload}
        visibilityControl={
          allPersons.length > 0 ? (
            <AssigneeVisibilityCombobox
              assignees={allPersons}
              hiddenAssignees={hiddenPersons}
              onToggle={togglePersonVisibility}
              disabled={hoursReport.status === "generating"}
            />
          ) : null
        }
        exportButton={
          showExport ? (
            <Button
              variant="default"
              onClick={() => void onExportExcel()}
              disabled={downloadingExcel}
            >
              {downloadingExcel ? (
                <>
                  <Loader2 className="size-4 animate-spin" aria-hidden />
                  Descargando...
                </>
              ) : (
                <>
                  <Download className="size-4" aria-hidden />
                  Exportar Excel
                </>
              )}
            </Button>
          ) : null
        }
      />

      {showStale ? (
        <ReportsTimeLogStaleBanner />
      ) : null}

      {showError ? <CopilotErrorAlert message={hoursReport.errorMessage ?? "Error desconocido"} /> : null}

      {hoursReport.result !== null ? (
        <ReportsTimeLogAlerts alerts={hoursReport.result.alerts} />
      ) : null}

      {hoursReport.status === "generating" ? (
        <ReportsTimeLogTableSkeleton />
      ) : (
        <>
          {showEmpty ? <ReportsTimeLogEmptyState /> : null}
          <ReportsTimeLogTable rows={filteredRows} />
        </>
      )}
    </div>
  );
}